		Béčku se bohužel moc nedařilo a skončilo poslední.</p>
	    <br/>
	    <p>Po velmi úspěšné přípravě vstoupilo áčko do podzimní části sezóny s velkými očekáváními.K prvnímu utkání 
		jsme nastoupili v téměř nejsilnější sestavě, pouze v bráně zaskakoval Přéma, ale vzhledem k jeho kvalitám 
