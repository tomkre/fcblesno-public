		kole utkalo s Vlčáky a neuspělo 6:9. Do konce podzimní části však áčko ztratilo body již jen jednou prohra 5:7 
		s týmem Fugas/Flamengo "C". To znamenalo, že áčko po podzimu vedlo okresní přebor s náskokem pěti bodů.</p>
