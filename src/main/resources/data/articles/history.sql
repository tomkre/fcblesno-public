	   <h2 id="5.sezona">2015/2016 &nbsp;<a href="#obsah">Zpět na obsah</a></h2>
	    <p>Již pátou letní přípravu jsme opět podpořili několika letními turnaji. Nejvíce se nám rozhodně povedl 
		turnaj v Libranticích, kde béčko získalo titul a áčko skončilo druhé. Velmi povedený byl také turnaj ve Skalici, 
		kde náš tým skončil druhý. Turnaje v Ledcích, Dohalicích, Českém Meziříčí a Blešně se nám již tolik výsledkově 
		nepovedli. Novinkou této přípravy také bylo, že jsme trénovali celé léto.</p>
	    <br/>
	    <p>Áčko do své historicky první sezóny v krajském přeboru nevstoupilo příliš dobře a pop prvních dvou kolech 
		a prohrách 4:8 a 1:7 se krčilo na posledním místě tabulky. Poté se však áčko herně a výsledkově zvedlo, což 
		vyvrcholilo výhrou nad Hesprem B v poměru 15:4, kde Honza Krejcar vstřelil 10 branek. V dalším zápase se nám 
		však zranil Kuba Krejcar, což bylo znát hlavně v zápase s lídrem tabulky Flamengem B, kde nebyl k dispozici 
		ani Michael Kotland a tak áčko podlehlo 2:9. Áčko i tak po podzimu obsadilo velmi pěkné 8. místo.</p>
	    <br/>
	    <p>Béčko do nové sezóny vstoupilo s jedinou posilou a tou byl Jaroslav Voltr z Bělče. Podzimní část sezóny 
		béčka hodně ovlivnily kontumační výhry, které béčko nasbíralo, byly nakonec tři. I tak béčko hodně pozitivně 
		překvapilo svými výsledky. První ztráta bodů se zrodila až v sedmém kole, ale ta stála za to. Béčko podlehlo 
		lídrovi soutěže v poměru 3:14. Béčko si připsalo i rekordní výhru 15:1.Do konce podzimu ztratilo béčko body 
		ještě jednou a to po hodně špatném výkonu, když doma podlehlo Atleticu Smidary 3:10. Další ztrátu bodů již béčko 
		nepřipustilo, a protože právě Atleticu bylo po skončení podzimní části odebráno 9 bodů, přezimovalo béčko na 
		skvělém druhém místě s náskokem pěti bodů na třetí tým tabulky 1.FK Újezd.</p>
	    <br/>
	    <p>Zimní příprava probíhala opět v Třebechovické tělocvičně, kde se konaly tréninky 1xtýdně ve středu, 
		docházka v roce 2015 byla spíše podprůměrná, naštěstí od konce ledna se docházka hráčů dostala na velmi 
		slušnou úroveň. Během zimní přípravy jsme se zúčastnili pouze jednoho turnaje, který jsme i sami pořádali, 
		po výsledkové stránce se nám turnaj moc nepovedl, ale po organizační dopadl velmi dobře.</p>
	    <br/>
